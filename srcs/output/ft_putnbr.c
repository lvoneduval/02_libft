#include "libft.h"

void	ft_putnbr(int n)
{
	int nbr[3];

	nbr[1] = n;
	nbr[0] = 1;
	if (n < 0)
	{
		ft_putchar('-');
		if (n == -2147483648)
		{
			ft_putchar(50);
			nbr[1] = 147483648;
		}
		else
			nbr[1] = -n;
	}
	nbr[2] = nbr[1];
	while ((nbr[2] /= 10) > 0)
		nbr[0] = nbr[0] * 10;
	nbr[2] = nbr[1];
	while (nbr[0] > 0)
	{
		ft_putchar((nbr[2] / nbr[0]) + 48);
		nbr[2] %= nbr[0];
		nbr[0] /= 10;
	}
}
